liked2023 = []
liked2024 = []

let euro2023 = 0;
let euro2024 = 0;
let iteration = 0;
let country;

const score2023 = document.querySelector('#score-2023')
const score2024 = document.querySelector('#score-2024')
const roundContainer = document.querySelector('.round img')

const btn2023 = document.querySelector('#btn2023')
const btn2024 = document.querySelector('#btn2024')

const pcountry2023 = document.querySelector('#btn2023 .country')
const partist2023 = document.querySelector('#btn2023 .artist')
const psong2023 = document.querySelector('#btn2023 .song')

const pcountry2024 = document.querySelector('#btn2024 .country')
const partist2024 = document.querySelector('#btn2024 .artist')
const psong2024 = document.querySelector('#btn2024 .song')

const round = document.querySelector('.round');



btn2023.addEventListener('click', () => {
    euro2023++;
    liked2023.push(country);
    iteration++;
    initialization();
    
});

btn2024.addEventListener('click', () => {
    euro2024++;
    liked2024.push(country)
    iteration++;
    initialization();
    

});

function initialization(){
    score2023.textContent = euro2023;
    score2024.textContent = euro2024;
    if(iteration >= participaints2023.length){
        finish();
    }
    else{
        country = participaints2023[iteration]['country'];
    if(country == "Romania"){
        iteration++;
        country = participaints2023[iteration]['country'];
    }
    const obj2023 = participaints2023[iteration];
    const obj2024 = participaints2024.filter(obj => {
        return obj.country == country
    })[0];

    roundContainer.src = `assets/${country.toLowerCase()}.png`;

    pcountry2023.textContent = `${obj2023.country}-2023`
    partist2023.textContent = obj2023.artist;
    psong2023.textContent = obj2023.song;

    pcountry2024.textContent = `${obj2024.country}-2024`
    partist2024.textContent = obj2024.artist;
    psong2024.textContent = obj2024.song;

    }
    

}


function finish(){
   
        btn2023.disabled = true;
        btn2024.disabled = true;
        round.removeChild(roundContainer);

        const div2023 = document.createElement('div')
        div2023.classList.add('liked');
        round.appendChild(div2023)

        const div2024 = document.createElement('div')
        div2024.classList.add('liked');
        round.appendChild(div2024)


        liked2023.forEach((val,idx) => {
            div2023.textContent += val
            if(idx != liked2023.length-1){
                div2023.textContent += ", "
            }
        });

        liked2024.forEach((val,idx) => {
            div2024.textContent += val;
            if(idx != liked2024.length-1){
                div2024.textContent += ", "
            }
        })
 
    }

initialization();